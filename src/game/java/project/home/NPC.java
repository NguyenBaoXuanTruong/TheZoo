package game.java.project.home;

public abstract class NPC {

    protected abstract String getName();

    protected abstract void setName(String name);

    protected abstract Double getMau();

    protected abstract void setMau(Double mau);

    protected abstract Double getMoney();

    protected abstract void setMoney(Double money);

    protected abstract Double getDames();

    protected abstract void setDames(Double dames);

    protected abstract Double getBuff();

    protected abstract void setBuff(Double buff);

    protected abstract Double getNgoc();

    protected abstract void setNgoc(Double ngoc);
}